    private void buttonKonversiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonKonversiActionPerformed
 try {
        double nilaiInput;
        String satuanInput = comboBoxInput.getSelectedItem().toString();
        String satuanOutput = comboBoxOutput.getSelectedItem().toString();

        // Cek arah konversi
        if (radioInputToOutput.isSelected()) {
            nilaiInput = Double.parseDouble(textInput.getText());
        } else if (radioOutputToInput.isSelected()) {
            nilaiInput = Double.parseDouble(textOutput.getText());
            satuanInput = comboBoxOutput.getSelectedItem().toString();
            satuanOutput = comboBoxInput.getSelectedItem().toString();
        } else {
            JOptionPane.showMessageDialog(this, "Pilih arah konversi terlebih dahulu!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Lakukan konversi suhu
        double hasil = konversiSuhu(nilaiInput, satuanInput, satuanOutput);

        // Tampilkan hasil pada TextField sesuai arah konversi
        if (radioInputToOutput.isSelected()) {
            textOutput.setText(String.format("%.2f", hasil));
        } else {
            textInput.setText(String.format("%.2f", hasil));
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan angka yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_buttonKonversiActionPerformed

