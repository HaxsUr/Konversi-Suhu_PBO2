    private void textInputKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textInputKeyTyped
    char c = evt.getKeyChar();
    // Cek apakah karakter yang dimasukkan adalah angka, backspace, atau titik
    if (!Character.isDigit(c) && c != '.' && c != KeyEvent.VK_BACK_SPACE) {
        evt.consume();  // Abaikan input jika bukan angka, backspace, atau titik
    }
    // Cek agar tidak ada lebih dari satu titik desimal
    if (c == '.' && textInput.getText().contains(".")) {
        evt.consume();  // Abaikan jika sudah ada titik
    }
    }//GEN-LAST:event_textInputKeyTyped

