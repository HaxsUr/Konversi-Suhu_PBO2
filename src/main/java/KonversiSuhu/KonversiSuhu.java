private double konversiSuhu(double nilai, String satuanInput, String satuanOutput) {
    double hasil = 0;

        // Konversi dari satuanInput ke Celsius
        switch (satuanInput) {
            case "Celsius" -> hasil = nilai; // Sudah dalam Celsius
            case "Fahrenheit" -> hasil = (nilai - 32) * 5 / 9; // Konversi Fahrenheit ke Celsius
            case "Kelvin" -> hasil = nilai - 273.15; // Konversi Kelvin ke Celsius
            case "Reamur" -> hasil = nilai * 5 / 4; // Konversi Reamur ke Celsius
            default -> {
            }
        }

        // Konversi dari Celsius ke satuanOutput
        switch (satuanOutput) {
            case "Celsius" -> {
                return hasil; // Sudah dalam Celsius
            }
            case "Fahrenheit" -> {
                return (hasil * 9 / 5) + 32; // Konversi Celsius ke Fahrenheit
            }
            case "Kelvin" -> {
                return hasil + 273.15; // Konversi Celsius ke Kelvin
            }
            case "Reamur" -> {
                return hasil * 4 / 5; // Konversi Celsius ke Reamur
            }
            default -> {
            }
        }

    // Jika satuanInput dan satuanOutput tidak valid
    return hasil; // Mengembalikan hasil
}
